package com.example.demo.dto;

public class UserWidgetDto {
    private String name;
    private Integer x;
    private Integer y;
    private Integer w;
    private Integer h;

    public String getName() {
        return name;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }

    public Integer getW() {
        return w;
    }

    public Integer getH() {
        return h;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setX(Integer x) {
        this.x = x;
    }

    public void setY(Integer y) {
        this.y = y;
    }

    public void setW(Integer w) {
        this.w = w;
    }

    public void setH(Integer h) {
        this.h = h;
    }
}
